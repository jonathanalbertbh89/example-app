     * O REST é baseado em princípios de arquitetura que utilizam o protocolo HTTP para realizar operações CRUD (Create, Read, Update e Delete) em recursos.
     * Os métodos HTTP mais comuns utilizados no REST são:
     * - GET: utilizado para recuperar informações de um recurso.
